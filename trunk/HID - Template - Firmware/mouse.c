static const BYTE pinMap[] = {0, 0, 0, 0, 0, 1, 2, 3, 6, 7, 4, 5};
INT tris_sel[2] = {&TRISC,&TRISB};
INT lat_sel[2] = {&LATC,&LATB};
void wait_a_bit()
{
	int i = 50;
	while(i--);
}

WORD SampleChannel(AnalogPins ch)
{
	
	AnalogPins chargePin;
	INT tris_p = 0,lat_p = 0;	

	chargePin = (ch==WHEELUP?WHEELDOWN:WHEELUP);

	ADCON0 = (chargePin << 2) | 1;
	//carico a 1
	*((BYTE *)lat_sel[chargePin/10]) |= (1<<pinMap[chargePin]);
	*((BYTE *)tris_sel[chargePin/10]) &= ~(1<<pinMap[chargePin]);
	//carico a 0
	*((BYTE *)lat_sel[ch/10]) &= ~(1<<pinMap[ch]);
	*((BYTE *)tris_sel[ch/10]) &= ~(1<<pinMap[ch]);
	
	wait_a_bit();

	*((BYTE *)tris_sel[ch/10]) |= (1<<pinMap[ch]);
	ADCON0 = (ch << 2) | 1;
	*((BYTE *)tris_sel[chargePin/10]) |= (1<<pinMap[chargePin]);
	
	ADCON0bits.GO = 1;
	while (ADCON0bits.GO);

	return ADRES;
}

/********************************************************************
 * Function:        void ProcessIO(void)
*	
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        This function is a place holder for other user
 *                  routines. It is a mixture of both USB and
 *                  non-USB tasks.
 *
 * Note:            None
 *******************************************************************/
void ProcessIO(void)
{  
    int i;
	// User Application USB tasks
    AnalogPins pin;

	for(i=0; i<12;i++)valori[i]=0;
	for(i=0;i<8;i++)
	{
		for (pin = 4; pin <12; pin++)
			valori[pin] += SampleChannel(pin);
	}
	if((USBDeviceState < CONFIGURED_STATE)||(USBSuspendControl==1)) return;
	hid_report_in[1]=0;
	hid_report_in[2]=0;
	hid_report_in[3]=0;
	
	for(i=4;i<12;i++)
		valori[i]/=8;

	if(valori[LEFT]<512)
		hid_report_in[1]-=sensibilita,something=1;
	if(valori[RIGHT]<512)
		hid_report_in[1]+=sensibilita,something=1;
	if(valori[UP]<400)
		hid_report_in[2]-=sensibilita,something=1;
	if(valori[DOWN]<512)
		hid_report_in[2]+=sensibilita,something=1;
	if(valori[WHEELUP]<512)
		hid_report_in[3]+=1,something=1;
	if(valori[WHEELDOWN]<512)
		hid_report_in[3]-=1,something=1;
	if(checked == 0)
	{
		if(valori[TASTO_SX]<512 != (hid_report_in[0]&1))
			hid_report_in[0]^=1,something=1;
		if(valori[TASTO_DX]<512 != (hid_report_in[0]&2))
			hid_report_in[0]^=2,something=1;	
	}else
	{
		if(valori[TASTO_SX]<512 != (hid_report_in[0]&2))
			hid_report_in[0]^=2,something=1;
		if(valori[TASTO_DX]<512 != (hid_report_in[0]&1))
			hid_report_in[0]^=1,something=1;	
	}
	if(something)
		invia_report();
}//end ProcessIO


