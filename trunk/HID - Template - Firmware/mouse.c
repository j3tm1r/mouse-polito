/******************************************************************************
 * Function:        void ParseFeatureReport(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          none
 *
 * Side Effects:    None
 *
 * Overview:        Parse feature report received from the host
 *
 * Note:            
 *
 *****************************************************************************/
void ParseFeatureReport(void)
{
	/* Got the new thresholds, store them to EEPROM and to the ADCThresholds array */
	size_t i;	
	BYTE controllo = 0;
	for (i = 0; i < FEATURE_SIZE-1; i++)
	{
		Busy_eep();
		Write_b_eep(i, FeatureReport[i]);
		controllo +=FeatureReport[i]; 
	}
	Busy_eep();
	Write_b_eep(i,controllo);
	new_settings();
}


